type Translations = typeof translations.en;

const LANG_STORAGE_KEY = "entr_lang";

function loadStoredLang(): Lang {
  try {
    const stored = localStorage.getItem(LANG_STORAGE_KEY);
    if (stored && stored in translations) return stored as Lang;
  } catch { /* private mode etc. */ }
  return "en";
}

/**
 * Translated strings can run longer than English. `fitFont` shaves at most
 * 1px off the given size for the wordier languages so longer text fits the
 * same layout box — images and containers must never resize with language.
 */
function makeFitFont(lang: Lang) {
  const verbose = lang === "es" || lang === "fr" || lang === "pt" || lang === "vi";
  return (base: number) => (verbose ? Math.max(base - 1, 10) : base);
}

const LanguageContext = createContext<{
  lang: Lang;
  currentLang: Lang;
  setLang: (l: Lang) => void;
  t: Translations;
  fitFont: (base: number) => number;
}>({
  lang: "en",
  currentLang: "en",
  setLang: () => {},
  t: translations.en,
  fitFont: (base) => base,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(loadStoredLang);

  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem(LANG_STORAGE_KEY, l); } catch { /* ignore */ }
  };

  return (
    <LanguageContext.Provider
      value={{
        lang,
        currentLang: lang,
        setLang,
        t: translations[lang],
        fitFont: makeFitFont(lang),
      }}
    >
      {children}
    </LanguageContext.Provider>
  );
}

export function useLang() {
  return useContext(LanguageContext);
}
